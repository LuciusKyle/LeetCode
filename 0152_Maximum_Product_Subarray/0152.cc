class Solution {
 public:
  int maxProduct(const vector<int>& nums) {
    if (nums.size() == 1) return nums[0];
    if (nums.size() == 2) return std::max(nums[0] * nums[1], std::max(nums[0], nums[1]));
    const auto mid_iter = nums.cbegin() + nums.size() / 2 + ((nums.size() % 2) > 0 ? 1 : 0);
    auto re_mid_iter = nums.crbegin() + nums.size() / 2;
    int left_max = *re_mid_iter, left_min = *re_mid_iter;
    int product = 1;
    for (; re_mid_iter != nums.crend(); ++re_mid_iter) {
      product *= *re_mid_iter;
      if (product < left_min) left_min = product;
      if (left_max < product) left_max = product;
    }
    product = 1;
    int right_max = *mid_iter, right_min = *mid_iter;
    for (auto temp_iter = mid_iter; temp_iter != nums.cend(); ++temp_iter) {
      product *= *temp_iter;
      if (product < right_min) right_min = product;
      if (right_max < product) right_max = product;
    }
    return std::max({maxProduct({nums.cbegin(), mid_iter}), maxProduct({mid_iter, nums.cend()}), left_max * right_min, left_min * right_max, left_max * right_max, left_min * right_min});
  }

 private:

  // minProduct is not used in this solution.
  int minProduct(const vector<int>& nums) {
    if (nums.size() == 1) return nums[0];
    if (nums.size() == 2) return std::min(nums[0] * nums[1], std::min(nums[0], nums[1]));
    auto mid_iter = nums.cbegin() + nums.size() / 2 + ((nums.size() % 2) > 0 ? 1 : 0);
    auto re_mid_iter = nums.crbegin() + nums.size() / 2;
    int left_max = *re_mid_iter, left_min = *re_mid_iter;
    int product = 1;
    for (; re_mid_iter != nums.crend(); ++re_mid_iter) {
      product *= *re_mid_iter;
      if (product < left_min) left_min = product;
      if (left_max < product) left_max = product;
    }
    product = 1;
    int right_max = *mid_iter, right_min = *mid_iter;
    for (; mid_iter != nums.cend(); ++mid_iter) {
      product *= *mid_iter;
      if (product < right_min) right_min = product;
      if (right_max < product) right_max = product;
    }
    return std::min({minProduct({nums.cbegin(), mid_iter}), minProduct({mid_iter, nums.cend()}), left_max * right_min, left_min * right_max, left_min * right_min, left_max * right_max});
  }
};

