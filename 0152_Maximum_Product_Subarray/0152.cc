class Solution {
 public:
  int maxProduct(const vector<int>& nums) {
    int max_product = nums[0];
    const size_t vec_size = nums.size();
    for (size_t i = 0; i < vec_size; ++i) {
      int product = nums[i];
      if (max_product < product) max_product = product;
      for (size_t j = i + 1; j < vec_size; ++j)
        if (max_product < (product *= nums[j]))
          max_product = product;
    }
    return max_product;
  }
};

